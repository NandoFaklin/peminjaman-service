package com.nando.peminjamanservice.service;

import com.nando.peminjamanservice.VO.Anggota;
import com.nando.peminjamanservice.VO.ResponseTemplateVO;
import com.nando.peminjamanservice.VO.buku;
import com.nando.peminjamanservice.entity.Peminjaman;
import com.nando.peminjamanservice.repository.PeminjamanRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.web.client.RestTemplate;

/**
 *
 * @author ACER
 */
@Service
public class PeminjamanService {
    @Autowired
    private PeminjamanRepository peminjamanRepository;
    
    @Autowired
    private RestTemplate restTemplate;
    
    public Peminjaman savePeminjaman(Peminjaman peminjaman){
        return peminjamanRepository.save(peminjaman);
    }
    
    public ResponseTemplateVO getPeminjaman(Long peminjamanId){
        ResponseTemplateVO vo = new ResponseTemplateVO();
        Peminjaman peminjaman = 
                peminjamanRepository.findByPeminjamanId(peminjamanId);
        Anggota anggota = 
        restTemplate.getForObject("http://localhost:9001/anggota/"
                + peminjaman.getAnggotaId(), Anggota.class);
        buku buku = restTemplate.getForObject("http://localhost:9003/buku/"
                + peminjaman.getbukuId(), buku.class);
        vo.setPeminjaman(peminjaman);
        vo.setAnggota(anggota); 
        vo.setbuku(buku); 
        return vo;
    }
    
}
